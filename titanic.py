import pandas as pd
import numpy as np

titanic_data = pd.read_csv('tested.csv')  # I hope this is the right file name!

print("--- First 5 rows ---")
print(titanic_data.head())  # This shows the first few rows

print("\n--- Info about the data ---")
titanic_data.info()  # This gives info about columns and types

print("\n--- Some statistics ---")
print(titanic_data.describe())  # This gives count, mean, etc. for numbers

selected_data = titanic_data[['Survived', 'Pclass', 'Age', 'Sex', 'Fare']]
print("\n--- Selected data ---")
print(selected_data.head())

survived_passengers = titanic_data[titanic_data['Survived'] == 1]
print("\n--- Survived passengers ---")
print(survived_passengers.head())

print("\n--- Missing data before handling ---")
print(titanic_data.isnull().sum())  # Count missing values

titanic_data['Age'].fillna(titanic_data['Age'].median(), inplace=True)

titanic_data['Embarked'].fillna(titanic_data['Embarked'].mode()[0], inplace=True)

print("\n--- Missing data after handling ---")
print(titanic_data.isnull().sum())  # Count missing values again